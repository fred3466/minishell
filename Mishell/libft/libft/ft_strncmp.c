#include "../libft.h"

int	ft_strncmp(const char *str1, const char *str2, size_t t)
{
	unsigned int	i;

	i = 0;
	while (str1[i] && i < t)
	{
		if (str1[i] != str2[i])
			return ((unsigned char)str1[i] - (unsigned char)str2[i]);
		i++;
	}
	if (str2[i] == '\0' || i == t)
		return (0);
	else
		return (-str2[i]);
}
