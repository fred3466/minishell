#include "../libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_s;

	new_s = (t_list *)malloc(sizeof(t_list));
	if (!new_s)
		return (NULL);
	(*new_s).content = content;
	new_s->next = NULL;
	return (new_s);
}
